package model;

public class Restaurant {

    /** Field **/
    private int id;
    private String name;


    /** Methods **/
    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public void setId(int id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }
}
